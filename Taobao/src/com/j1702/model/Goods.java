package com.j1702.model;

public class Goods {
	
	private int id;
	private String name;
	private int num;
	private Float price;
	private String picture;
	private Float lowprice;
	private String goodexplain;
	private String lowpriceexplain;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public Float getPrice() {
		return price;
	}
	public void setPrice(Float price) {
		this.price = price;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public Float getLowprice() {
		return lowprice;
	}
	public void setLowprice(Float lowprice) {
		this.lowprice = lowprice;
	}
	public String getGoodexplain() {
		return goodexplain;
	}
	public void setGoodexplain(String goodexplain) {
		this.goodexplain = goodexplain;
	}
	public String getLowpriceexplain() {
		return lowpriceexplain;
	}
	public void setLowpriceexplain(String lowpriceexplain) {
		this.lowpriceexplain = lowpriceexplain;
	}
	
	
	
	
}
